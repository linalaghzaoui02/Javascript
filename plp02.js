// Integer (Note: JavaScript does not differentiate between integer and float)
let integerVariable = 42;
console.log("Integer:", integerVariable);

// String
let stringVariable = "Hello, World";
console.log("String:", stringVariable);

// Floating-point number
let floatVariable = 42.42;
console.log("Float:", floatVariable);

// Boolean
let booleanVariable = true;
console.log("Boolean:", booleanVariable);

// Array
let arrayVariable = [1, 2, 3];
console.log("Array:", arrayVariable);

// Object (Equivalent to dictionary, hash, map)
let objectVariable = {
  key1: "value1",
  key2: "value2"
};
console.log("Object:", objectVariable);
